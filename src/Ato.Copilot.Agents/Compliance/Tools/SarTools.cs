public class SarGenerateTool : BaseTool
{
    private readonly ISecurityAssessmentReportService _service;

    public SarGenerateTool(
        ISecurityAssessmentReportService service,
        ILogger<SarGenerateTool> logger) : base(logger)
    {
        _service = service;
    }

    public override string Name => "compliance_sar_generate";

    public override string Description =>
        "Generate a Security Assessment Report from existing assessment data. " +
        "Auto-populates findings sections and creates editable narrative sections. " +
        "RBAC: SCA, ISSM, or AO.";

    public override IReadOnlyDictionary<string, ToolParameter> Parameters => new Dictionary<string, ToolParameter>
    {
        ["system_id"] = new() { Name = "system_id", Description = "System GUID, name, or acronym", Type = "string", Required = true },
        ["title"] = new() { Name = "title", Description = "SAR document title", Type = "string", Required = true },
        ["sap_id"] = new() { Name = "sap_id", Description = "Optional governing SAP ID", Type = "string", Required = false }
    };

    public override async Task<string> ExecuteCoreAsync(
        Dictionary<string, object?> arguments,
        CancellationToken cancellationToken = default)
    {
        var sw = Stopwatch.StartNew();
        var systemId = GetArg<string>(arguments, "system_id");
        var title = GetArg<string>(arguments, "title");
        var sapId = GetArg<string>(arguments, "sap_id");

        if (string.IsNullOrWhiteSpace(systemId))
            return Error("INVALID_INPUT", "The 'system_id' parameter is required.");
        if (string.IsNullOrWhiteSpace(title))
            return Error("INVALID_INPUT", "The 'title' parameter is required.");

        try
        {
            var request = new CreateSarRequest { Title = title, SapId = sapId };
            var sar = await _service.CreateSarAsync(systemId, request, "mcp-user", cancellationToken);

            sw.Stop();
            return JsonSerializer.Serialize(new
            {
                status = "success",
                data = new
                {
                    sar_id = sar.Id,
                    system_id = sar.RegisteredSystemId,
                    title = sar.Title,
                    sar_status = sar.Status.ToString(),
                    total_controls_assessed = sar.TotalControlsAssessed,
                    satisfied_count = sar.SatisfiedCount,
                    not_satisfied_count = sar.NotSatisfiedCount,
                    sections = sar.Sections.Select(s => new
                    {
                        section_type = s.SectionType.ToString(),
                        title = s.Title,
                        is_auto_generated = s.IsAutoGenerated,
                        has_content = !string.IsNullOrWhiteSpace(s.Content)
                    }),
                    created_by = sar.CreatedBy,
                    created_at = sar.CreatedAt.ToString("O")
                },
                metadata = Meta(sw)
            }, JsonOpts);
        }
        catch (InvalidOperationException ex)
        {
            return Error("SAR_GENERATION_FAILED", ex.Message);
        }
    }

    private static readonly JsonSerializerOptions JsonOpts = new() { PropertyNamingPolicy = JsonNamingPolicy.CamelCase, WriteIndented = true };

    private static string Error(string code, string message) =>
        JsonSerializer.Serialize(new { status = "error", errorCode = code, message }, new JsonSerializerOptions { WriteIndented = true });

    private object Meta(Stopwatch sw) => new { tool = Name, duration_ms = sw.ElapsedMilliseconds, timestamp = DateTime.UtcNow.ToString("O") };
}

