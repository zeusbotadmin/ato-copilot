public class SarReviewTool : BaseTool
{
    private readonly ISecurityAssessmentReportService _service;

    public SarReviewTool(
        ISecurityAssessmentReportService service,
        ILogger<SarReviewTool> logger) : base(logger)
    {
        _service = service;
    }

    public override string Name => "compliance_sar_review";

    public override string Description =>
        "Submit a SAR for review, or approve/request revision. " +
        "Use action='submit' to transition Draft→UnderReview. " +
        "Use action='approve' or action='request_revision' to review. " +
        "RBAC: SCA/ISSM for submit, ISSM/AO for review.";

    public override IReadOnlyDictionary<string, ToolParameter> Parameters => new Dictionary<string, ToolParameter>
    {
        ["sar_id"] = new() { Name = "sar_id", Description = "SAR ID (GUID)", Type = "string", Required = true },
        ["action"] = new() { Name = "action", Description = "Action: submit, approve, request_revision", Type = "string", Required = true },
        ["comments"] = new() { Name = "comments", Description = "Optional reviewer comments (for approve/request_revision)", Type = "string", Required = false }
    };

    public override async Task<string> ExecuteCoreAsync(
        Dictionary<string, object?> arguments,
        CancellationToken cancellationToken = default)
    {
        var sw = Stopwatch.StartNew();
        var sarId = GetArg<string>(arguments, "sar_id");
        var action = GetArg<string>(arguments, "action");
        var comments = GetArg<string>(arguments, "comments");

        if (string.IsNullOrWhiteSpace(sarId))
            return Error("INVALID_INPUT", "The 'sar_id' parameter is required.");
        if (string.IsNullOrWhiteSpace(action))
            return Error("INVALID_INPUT", "The 'action' parameter is required.");

        try
        {
            SecurityAssessmentReport sar;

            switch (action.ToLowerInvariant())
            {
                case "submit":
                    sar = await _service.SubmitForReviewAsync(sarId, "mcp-user", cancellationToken);
                    break;
                case "approve":
                    sar = await _service.ReviewSarAsync(sarId, new ReviewSarRequest { Decision = "approve", Comments = comments }, "mcp-user", cancellationToken);
                    break;
                case "request_revision":
                    sar = await _service.ReviewSarAsync(sarId, new ReviewSarRequest { Decision = "request_revision", Comments = comments }, "mcp-user", cancellationToken);
                    break;
                default:
                    return Error("INVALID_INPUT", $"Invalid action '{action}'. Valid values: submit, approve, request_revision.");
            }

            sw.Stop();
            return JsonSerializer.Serialize(new
            {
                status = "success",
                data = new
                {
                    sar_id = sar.Id,
                    sar_status = sar.Status.ToString(),
                    modified_by = sar.ModifiedBy,
                    modified_at = sar.ModifiedAt?.ToString("O"),
                    message = action.ToLowerInvariant() switch
                    {
                        "submit" => "SAR submitted for review.",
                        "approve" => "SAR approved.",
                        "request_revision" => "Revision requested. SAR returned to Draft status.",
                        _ => "Action completed."
                    }
                },
                metadata = Meta(sw)
            }, JsonOpts);
        }
        catch (InvalidOperationException ex)
        {
            return Error("REVIEW_FAILED", ex.Message);
        }
    }

    private static readonly JsonSerializerOptions JsonOpts = new() { PropertyNamingPolicy = JsonNamingPolicy.CamelCase, WriteIndented = true };

    private static string Error(string code, string message) =>
        JsonSerializer.Serialize(new { status = "error", errorCode = code, message }, new JsonSerializerOptions { WriteIndented = true });

    private object Meta(Stopwatch sw) => new { tool = Name, duration_ms = sw.ElapsedMilliseconds, timestamp = DateTime.UtcNow.ToString("O") };
}
