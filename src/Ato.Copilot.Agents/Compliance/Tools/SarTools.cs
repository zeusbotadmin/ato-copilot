public class SarEditSectionTool : BaseTool
{
    private readonly ISecurityAssessmentReportService _service;

    public SarEditSectionTool(
        ISecurityAssessmentReportService service,
        ILogger<SarEditSectionTool> logger) : base(logger)
    {
        _service = service;
    }

    public override string Name => "compliance_sar_edit_section";

    public override string Description =>
        "Edit a narrative section of a Security Assessment Report. " +
        "Only works on editable sections (ExecutiveSummary, AssessmentScope, Recommendations) " +
        "when SAR is in NotStarted or Draft status. " +
        "RBAC: SCA, ISSM, or AO.";

    public override IReadOnlyDictionary<string, ToolParameter> Parameters => new Dictionary<string, ToolParameter>
    {
        ["sar_id"] = new() { Name = "sar_id", Description = "SAR ID (GUID)", Type = "string", Required = true },
        ["section_type"] = new() { Name = "section_type", Description = "Section type: ExecutiveSummary, AssessmentScope, Recommendations", Type = "string", Required = true },
        ["content"] = new() { Name = "content", Description = "New section content (Markdown)", Type = "string", Required = true }
    };

    public override async Task<string> ExecuteCoreAsync(
        Dictionary<string, object?> arguments,
        CancellationToken cancellationToken = default)
    {
        var sw = Stopwatch.StartNew();
        var sarId = GetArg<string>(arguments, "sar_id");
        var sectionTypeStr = GetArg<string>(arguments, "section_type");
        var content = GetArg<string>(arguments, "content");

        if (string.IsNullOrWhiteSpace(sarId))
            return Error("INVALID_INPUT", "The 'sar_id' parameter is required.");
        if (string.IsNullOrWhiteSpace(sectionTypeStr))
            return Error("INVALID_INPUT", "The 'section_type' parameter is required.");
        if (string.IsNullOrWhiteSpace(content))
            return Error("INVALID_INPUT", "The 'content' parameter is required.");

        if (!Enum.TryParse<SarSectionType>(sectionTypeStr, true, out var sectionType))
            return Error("INVALID_INPUT", $"Invalid section_type '{sectionTypeStr}'. Valid values: ExecutiveSummary, AssessmentScope, Recommendations.");

        try
        {
            var request = new EditSarSectionRequest { Content = content };
            var section = await _service.EditSectionAsync(sarId, sectionType, request, "mcp-user", cancellationToken);

            sw.Stop();
            return JsonSerializer.Serialize(new
            {
                status = "success",
                data = new
                {
                    section_type = section.SectionType.ToString(),
                    title = section.Title,
                    content_length = section.Content?.Length ?? 0,
                    modified_by = section.ModifiedBy,
                    modified_at = section.ModifiedAt?.ToString("O")
                },
                metadata = Meta(sw)
            }, JsonOpts);
        }
        catch (InvalidOperationException ex)
        {
            return Error("EDIT_FAILED", ex.Message);
        }
    }

    private static readonly JsonSerializerOptions JsonOpts = new() { PropertyNamingPolicy = JsonNamingPolicy.CamelCase, WriteIndented = true };

    private static string Error(string code, string message) =>
        JsonSerializer.Serialize(new { status = "error", errorCode = code, message }, new JsonSerializerOptions { WriteIndented = true });

    private object Meta(Stopwatch sw) => new { tool = Name, duration_ms = sw.ElapsedMilliseconds, timestamp = DateTime.UtcNow.ToString("O") };
}

